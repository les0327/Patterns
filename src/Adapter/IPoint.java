package Adapter;

/**
 * Interface for point
 *
 * @author  - Vova
 * @version - 4.0
 */
public interface IPoint {

    /**
     * Set x coordinate of point
     *
     * @param x - x coordinate of point
     */
    void setX(int x);


    /**
     * Set y coordinate of point
     *
     * @param y - y coordinate of point
     */
    void setY(int y);
}


